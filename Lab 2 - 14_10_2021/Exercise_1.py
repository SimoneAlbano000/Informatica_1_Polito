# The code will execute some math operations on two constant values

CONST_1 = 5
CONST_2 = 10

print("Sum:", CONST_1+CONST_2)
print("Difference:", CONST_1-CONST_2)
print("Multiplication:", CONST_1*CONST_2)
print("Mid-value:", (CONST_1+CONST_2)/2)
print("Point-distance:", abs(CONST_1-CONST_2))
print("Max:", max(CONST_1, CONST_2))
print("Min:", min(CONST_1, CONST_2))